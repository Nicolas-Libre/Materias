package arbolBinario;

public class sumaYDif {
	private int suma=0;
	private int resta = 0;
	
	public void setearAmbos (int suma, int resta) {
		this.suma = suma;
		this.resta = resta;
	}

	public int getSuma() {
		return suma;
	}

	public void setSuma(int suma) {
		this.suma = suma;
	}

	public int getResta() {
		return resta;
	}

	public void setResta(int resta) {
		this.resta = resta;
	}
	
	
}
