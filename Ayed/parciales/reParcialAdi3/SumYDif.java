package reParcialAdi3;

public class SumYDif {

	private int suma;
	private int dif;
	
	
	public SumYDif (int suma, int dif) {
		setSuma (suma);
		setDif (dif);
	}
	
	public int getSuma() {
		return suma;
	}
	public void setSuma(int suma) {
		this.suma = suma;
	}
	public int getDif() {
		return dif;
	}
	public void setDif(int dif) {
		this.dif = dif;
	}
	
	
	
	
}
